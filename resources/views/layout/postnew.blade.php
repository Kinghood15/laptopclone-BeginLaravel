				<div class="news-title">
					<h2>Tầm quan trọng của bột in 3D công nghệ SLS</h2>
				</div>
				<div class="news-time">
					<p><i class="fas fa-circle"></i> Tin tức </p>
					<p> lúc <span>12/02/2021</span></p>
					<p> bởi <span>An Alex Nguyễn</span></p>
				</div>
				<div class="news-share">
					<a href="#"><p id="facebook-share"><i class="fab fa-facebook-f"></i> Facebook</p></a>
					<a href="#"><p id="instagram-share"><i class="fab fa-instagram"></i> Instagram</p></a>
					<a href="#"><p id="twitter-share"><i class="fab fa-twitter"></i> Twiter</p></a>
					<a href="#"><p id="google-plus-share"><i class="fab fa-google-plus-g"></i> Google+</p></a>
					<a href="#"><p id="linkedin-share"><i class="fab fa-linkedin"></i> Linkedin</p></a>
				</div>
				<div class="news-content">
					<p>In 3D phát triển và trưởng thành để thực hiện những kỳ vọng xung quanh nó. Và để đáp ứng những kỳ vọng ngày càng tăng này, và giải quyết ngày càng nhiều trường hợp ứng dụng in 3D: Trong sản xuất tạo mẫu nhanh và chế thử. Điều quan trọng ta phải nhận ra rằng: Bản thân máy in 3D chỉ là một phần của phương trình, một yếu tố quan trọng khác là lựa chọn vật liệu. Trong trường hợp này là vật liệu bột in 3D SLS.</p>
					<div class="news-img">
						<img src="./image/formlabs-fuse-1-sls-01-1024x867.jpg">
					</div>
					<p>Đặc biệt xem xét kỹ thuật thiêu kết laser chọn lọc (SLS), công nghệ này ban đầu được kết hợp với nhiều loại vật liệu. Cũng như các bản in có độ chính xác cao; Và khả năng tạo ra các hình học phức tạp mà không cần vật liệu hỗ trợ. Đấy là khi SLS trở thành một quy trình độc quyền; Phần lớn là do chi phí cao và tính chất công nghiệp của nó.</p>
					<p>Tuy nhiên, điều này bắt đầu thay đổi vào năm 2014. Khi bằng sáng chế SLS hết hạn và các công ty, chẳng hạn như Formlabs, Sinterit, Natural Robotics, Red Rock 3D tham gia thị trường với các hệ thống để bàn giá cả phải chăng hơn. Những hệ thống mới này, mặc dù rẻ hơn mười lần so với máy in SLS công nghiệp, nhưng không có cùng mức độ tương thích với vật liệu. Nhưng tại sao?</p>
					<div class="news-heading1-title">
						<h2>Cách loại laser ảnh hưởng đến sự lựa chọn bột SLS</h2>
					</div>
					<p>Một trong những điều chính cần lưu ý ngay lập tức là: Các hệ thống SLS để bàn không được phát triển đơn giản bằng cách thu nhỏ các máy SLS công nghiệp. Cấu hình mới phải được thiết kế. Chẳng hạn, laser CO2 trong các hệ thống công nghiệp thường có chiều dài hơn một mét, điều này không cho phép chúng được sử dụng trong các hệ thống máy in 3D để bàn. Máy SLS công nghiệp cũng tích hợp một hệ thống gương phức tạp để điều khiển hướng của chùm tia laze.</p>
					<div class="news-img">
						<img src="./image/formlabs-fuse-1-sls-02-300x169.jpg">
					</div>
					<p>Mặt khác, trong các máy in 3D SLS cỡ vừa và nhỏ, một diode laser hồng ngoại nhỏ gọn hơn được sử dụng thay vì laser CO2. Loại laser này, mặc dù có khả năng đạt được độ chính xác như laser CO2, nhưng không tương thích với các vật liệu trắng. (Những người quen thuộc với SLS công nghiệp sẽ biết rằng nhiều vật liệu bột được sử dụng có màu trắng.)</p>
					<div class="news-heading1-title">
						<h2>Vật liệu nào cho máy in 3D SLS để bàn?</h2>
					</div>
					<p>Trong khi máy in 3D diode laser không tương thích với vật liệu trắng; Điều đó không có nghĩa là chúng bị hạn chế về tính đa dạng vật liệu. Ví dụ, hệ thống SLS để bàn có thể in các vật liệu PA, TPU và TPE, bên cạnh những vật liệu khác. Tuy nhiên, có một số yếu tố ảnh hưởng đến mức độ hiệu quả khi phải in các loại vật liệu khác nhau trên một máy in 3D SLS.</p>
					<p>Để cải thiện sự lựa chọn vật liệu và sự tự do cho người dùng cuối: Máy in 3D phải dễ làm sạch sau mổi lần thay đổi vật liệu; Và có thể điều chỉnh thông số in cho mỗi loại vật liệu khác nhau. Đặc biệt là khi chuyển đổi giữa vật liệu có nhiệt độ (điểm) nóng chảy thấp hơn như TPE; Và vật liệu có điểm nóng chảy cao hơn như PA.</p>
					<div class="news-heading1-title">
						<h2>Một vật liệu cho mọi nhu cầu</h2>
					</div>
					<p>SLS không chỉ là giải pháp in 3D phổ biến cho nhu cầu sản xuất các bộ phận (parts) có độ chính xác và độ bền về kích thước. công nghệ này còn có thể giải quyết các đặc tính khác như: Cách điện, độ dẫn điện, tính linh hoạt và độ kín. Người dùng cuối cần một loạt các vật liệu để lựa chọn để phù hợp với các thuộc tính mà họ yêu cầu cho ứng dụng tạo mẫu hoặc sử dụng cuối của họ.</p>
					<p>Formlabs nhận ra tầm quan trọng của vật liệu in 3d SLS cho các máy in SLS để bàn. Các kỹ sư R	&#38;D Formlabs đã phát triển loại bột Nylon tương thích cho quá trình thiêu kết laser chọn lọc; Và đáp ứng các đặc tính kỹ thuật cần thiết.</p>
					<div class="news-heading1-title">
						<h3>Nylon PA12, vật liệu SLS hàng đầu</h3>
					</div>
					<p>Như đã đề cập, PA12 là bột in 3D SLS phổ biến nhất. Vật liệu không chỉ tự hào về tính linh hoạt và các đặc tính cơ học tốt (chẳng hạn như độ bền), mà còn tiết kiệm chi phí. Trong SLS, bản thân chi phí mua hàng không quá đáng kể. Thay vào đó, tỷ lệ làm mới thực sự ảnh hưởng đến chi phí (có nghĩa là, bao nhiêu bột có thể được tái sử dụng). PA12 có tỷ lệ làm tươi thấp: tiêu chuẩn thị trường là 50-70%. Và Formlabs đã đạt được tỷ lệ làm mới 30% ~ 50%.</p>
					<div class="news-heading1-title">
						<h3>Nylon PA11, chống va đập cao</h3>
					</div>
					<div class="news-img">
						<img src="./image/formlabs-fuse-1-sls-04.jpg">
					</div>
					<p>Nếu Nylon 12 là vật liệu được sử dụng rộng rãi nhất cho SLS; Thì Nylon 11 lại là một lựa chọn phổ biến. Bột polyamide (nylon) có khả năng chịu nhiệt cao và ổn định ánh sáng, tia cực tím và thời tiết. Vật liệu này thường được sử dụng để sản xuất: Đồ gá, và các phần tử công cụ, cũng như prototype mẫu vỏ (covers, housings) và các bộ phận lắp ráp. Điều đáng chú ý là vật liệu cần có môi trường yếm khí để in, và do đó chỉ tương thích với máy in FUSE 1 có nitơ.</p>
